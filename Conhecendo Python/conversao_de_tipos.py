#inteiro para float

preco = 10
print(preco)

preco = float(preco)
print(preco)

preco = 10/2
print(preco)

#float para inteiro
preca = 10.30
print(preca)

preca = int(preca)
print(preca)

#conversão para divisão

dindin = 10
print(dindin)

print(dindin / 2)
print(dindin // 2)

#numérico para string 

gasto = 10.50
idade = 28
print(str(gasto))

print(str(idade))

texto = f"idade {idade} preco {dindin}"
print(texto)

#String para número

money = "10.50"
idade = "28"

print(float(money))

print(int(idade))